#include "minirt.h"

int	ft_rgb_to_i(t_rgb rgb)
{
	int	num;

	num = 0;
	num += (rgb.r * 255) * 0x010000;
	num += (rgb.g * 255) * 0x000100;
	num += (rgb.b * 255) * 0x000001;
	return (num);
}
