#include "minirt.h"

int	ft_rgb_to_i(t_rgb rgb)
{
	int	num;

	num = 0;
	num += (int)(rgb.r *255) * 0x010000;
	num += (int)(rgb.g *255) * 0x000100;
	num += (int)(rgb.b *255) * 0x000001;
	return (num);
}
