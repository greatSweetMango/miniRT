#include "minirt.h"

t_rgb	ft_i_to_rgb(int value)
{
	t_rgb	rgb;

	rgb.x = ((value >> 16) & 0xFF) / 256.0;
	rgb.y = ((value >> 8) & 0xFF) / 256.0;
	rgb.z = (value & 0xFF) / 256.0;
	return (rgb);
}
